# ---------------------------------------------------------------------------
# SQL
# One row per HistoryDetail message.
# Parent conversation context (title, model, soft-deleted) is joined from
# History. Attachment presence is detected via correlated EXISTS subqueries —
# lightweight boolean checks that avoid duplicating rows from the attachment
# tables.
# ---------------------------------------------------------------------------
QUERY = """
SELECT
    -- Message identity
    hd.id                                               AS msg_id,
    hd.UUID                                             AS msg_uuid,
    hd.historyID                                        AS conv_id,

    -- Parent conversation context (from History)
    h.UUID                                              AS conv_uuid,
    h.title                                             AS conv_title,
    h.chatBotModel                                      AS chat_bot_model,
    h.softDeleted                                       AS soft_deleted,

    -- Message content
    hd.type                                             AS msg_type,
    hd.text                                             AS msg_text,
    hd.token                                            AS token_count,
    hd.reasoningContent                                 AS reasoning_content,

    -- Message timestamps
    hd.createdAt                                        AS created_at,
    hd.lastModifiedAt                                   AS last_modified_at,

    -- Sync metadata
    hd.syncState                                        AS sync_state,
    hd.syncRetryCount                                   AS sync_retry_count,

    -- Attachment flags (correlated EXISTS — no row multiplication)
    CASE WHEN EXISTS (
        SELECT 1 FROM HistoryDetailImage i
        WHERE i.historyDetailID = hd.id
    ) THEN 1 ELSE 0 END                                 AS has_image,

    CASE WHEN EXISTS (
        SELECT 1 FROM HistoryDetailDocument d
        WHERE d.historyDetailID = hd.id
    ) THEN 1 ELSE 0 END                                 AS has_document,

    CASE WHEN EXISTS (
        SELECT 1 FROM HistoryDetailLink l
        WHERE l.historyDetailID = hd.id
    ) THEN 1 ELSE 0 END                                 AS has_link

FROM HistoryDetail hd
INNER JOIN History h
    ON h.id = hd.historyID
ORDER BY hd.historyID ASC, hd.createdAt ASC
"""


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


def _convert_ms_timestamp(ms):
    """Convert a Unix millisecond timestamp to a human-readable UTC string."""
    if ms is None:
        return ""
    try:
        return datetime.datetime.utcfromtimestamp(ms / 1000).strftime(
            "%Y-%m-%d %H:%M:%S UTC"
        )
    except (OSError, OverflowError, ValueError):
        return str(ms)


def _resolve_model(model_int):
    """Return a labelled model name, falling back to the raw integer for unknowns."""
    if model_int is None:
        return "Unknown"
    name = CHAT_BOT_MODEL_MAP.get(model_int)
    return f"{name} ({model_int})" if name else f"Unknown Model ({model_int})"


def _format_role(type_int):
    """Map HistoryDetail.type to a forensically clear role label."""
    if type_int == 0:
        return "USER"
    if type_int == 1:
        return "ASSISTANT"
    return f"UNKNOWN ({type_int})"


def _format_soft_deleted(value):
    """Return a clearly labelled string for the softDeleted field."""
    return "DELETED" if value == 1 else "No"


def _flag(value):
    """Return Yes/No for a boolean integer flag."""
    return "Yes" if value else "No"


