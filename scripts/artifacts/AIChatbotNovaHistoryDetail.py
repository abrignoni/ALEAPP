def get_nova_chatbot_history_detail(files_found, report_folder, seeker, wrap_text):
    """
    Entry point for the nova_chatbot_history_detail artifact.

    Queries every message in HistoryDetail, enriched with parent conversation
    context from History and attachment presence flags from the three attachment
    tables. Outputs HTML report, TSV, and timeline.
    """

    for file_found in files_found:
        file_found = str(file_found)

        if not file_found.endswith("chat-ai.db"):
            continue

        try:
            db = sqlite3.connect(file_found)
            cursor = db.cursor()
            cursor.execute(QUERY)
            rows_raw = cursor.fetchall()
            db.close()

        except Exception as e:
            scripts.ilapfuncs.logfunc(
                f"[nova_chatbot_history_detail] Error reading {file_found}: {e}"
            )
            continue

        if not rows_raw:
            scripts.ilapfuncs.logfunc(
                f"[nova_chatbot_history_detail] No records found in {file_found}."
            )
            continue

        headers = [
            # --- Message identity ---
            "Msg. ID",
            "Msg. UUID",
            "Conv. ID",
            # --- Parent conversation context ---
            "Conv. UUID",
            "Conv. Title",
            "AI Model",
            "Conv. Deleted",
            # --- Message content ---
            "Role",
            "Message Text",
            "Token Count",
            "Reasoning Content",
            # --- Timestamps ---
            "Message Timestamp (UTC)",
            "Last Modified At (UTC)",
            # --- Sync metadata ---
            "Sync State",
            "Sync Retry Count",
            # --- Attachment flags ---
            "Has Image",
            "Has Document",
            "Has Link",
        ]

        rows = []
        for row in rows_raw:
            (
                msg_id,
                msg_uuid,
                conv_id,
                conv_uuid,
                conv_title,
                chat_bot_model,
                soft_deleted,
                msg_type,
                msg_text,
                token_count,
                reasoning_content,
                created_at,
                last_modified_at,
                sync_state,
                sync_retry_count,
                has_image,
                has_document,
                has_link,
            ) = row

            rows.append(
                (
                    msg_id,
                    msg_uuid or "",
                    conv_id,
                    conv_uuid or "",
                    conv_title or "",
                    _resolve_model(chat_bot_model),
                    _format_soft_deleted(soft_deleted),
                    _format_role(msg_type),
                    msg_text or "",
                    token_count if token_count is not None else "",
                    reasoning_content or "",
                    _convert_ms_timestamp(created_at),
                    _convert_ms_timestamp(last_modified_at),
                    sync_state if sync_state is not None else "",
                    sync_retry_count if sync_retry_count is not None else "",
                    _flag(has_image),
                    _flag(has_document),
                    _flag(has_link),
                )
            )

        # --- HTML report ---
        report_name = "Nova AI Chatbot - HistoryDetail"
        report = ArtifactHtmlReport(report_name)
        report.start_artifact_report(report_folder, report_name)
        report.add_script()
        report.write_artifact_data_table(
            headers,
            rows,
            file_found,
            html_escape=True,
        )
        report.end_artifact_report()

        # --- TSV output ---
        scripts.ilapfuncs.tsv(report_folder, headers, rows, report_name, file_found)

        # --- Timeline (message-level granularity, index 11 = Message Timestamp) ---
        scripts.ilapfuncs.timeline(report_folder, report_name, rows, headers)
