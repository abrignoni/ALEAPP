def _build_image_cell(
    img_url, prompt, state, mime_type, pipeline, style_id, msg_type, filename
):
    """
    Build HTML cell using msg_type to determine image origin.
    """
    origin_key, origin_label = _get_image_origin(msg_type)
    cell = '<div style="font-size:12px; line-height:1.8;">'

    if prompt:
        cell += f'<div style="margin-bottom:6px;"><strong>Prompt:</strong> {_e(prompt)}</div>'

    if origin_key == "ai-generated":
        cell += (
            f'<div style="padding:8px; background:#eaf4fb; border:1px solid #aed6f1; border-radius:4px; font-size:11px; margin-bottom:6px;">'
            f"  🤖 <strong>AI‑generated image</strong><br>"
            f"  This image was created by the AI based on the user prompt. "
            f"  It is stored on Firebase Storage; a temporary local copy may exist "
            f"  in <code>cache/image_manager_disk_cache/*.0</code> but the filename "
            f"  is a hash of a signed URL that includes a token not stored on the device. "
            f"  Manual inspection of <code>.0</code> files is recommended.<br>"
            f"  <strong>Forensic action:</strong> Examine <code>.0</code> files directly as JPEG."
            f"</div>"
        )
    elif origin_key == "user-submitted":
        cell += (
            f'<div style="padding:8px; background:#fef9e7; border-left:3px solid #f39c12; font-size:11px; margin-bottom:6px;">'
            f"  📤 <strong>User‑submitted image</strong><br>"
            f"  This image was actively uploaded by the device user (e.g., as part of a vision query). "
            f"  The file content is stored on Firebase Storage and is not cached locally. "
            f"  Only the metadata record remains on the device."
            f"</div>"
        )
    else:
        cell += (
            f'<div style="padding:8px; background:#e2e3e5; border:1px solid #6c757d; border-radius:4px; font-size:11px; margin-bottom:6px;">'
            f"  ❓ <strong>Unknown image origin</strong><br>"
            f"  The parent message type is {_e(str(msg_type))} – cannot determine if user‑submitted or AI‑generated."
            f"</div>"
        )

    if pipeline:
        cell += f"<div><strong>Pipeline:</strong> {_e(pipeline)}</div>"
    if style_id is not None:
        cell += f"<div><strong>Style ID:</strong> {_e(str(style_id))}</div>"
    if mime_type:
        cell += f"<div><strong>MIME Type:</strong> {_e(mime_type)}</div>"

    cell += (
        f'<div style="margin-top:4px; font-size:10px; color:#7f8c8d;">'
        f"  <strong>Firebase path:</strong><br>"
        f'  <code style="word-break:break-all;">{_e(img_url)}</code>'
        f"</div>"
    )
    cell += "</div>"
    return cell


