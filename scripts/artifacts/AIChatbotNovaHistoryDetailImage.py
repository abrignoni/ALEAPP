QUERY = """
SELECT
    i.id                    AS img_id,
    i.historyDetailID       AS msg_id,
    i.url                   AS img_url,
    i.prompt                AS prompt,
    i.state                 AS state,
    i.mimeType              AS mime_type,
    i.styleId               AS style_id,
    i.source                AS source,
    i.sourceUrl             AS source_url,
    i.pipeline              AS pipeline,

    hd.historyID            AS conv_id,
    hd.type                 AS msg_type,
    hd.text                 AS msg_text,
    hd.createdAt            AS msg_created_at,

    h.UUID                  AS conv_uuid,
    h.title                 AS conv_title,
    h.chatBotModel          AS chat_bot_model,
    h.softDeleted           AS soft_deleted

FROM HistoryDetailImage i
INNER JOIN HistoryDetail hd ON hd.id = i.historyDetailID
INNER JOIN History h        ON h.id = hd.historyID
ORDER BY i.id ASC
"""

