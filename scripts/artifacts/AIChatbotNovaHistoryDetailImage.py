# Entry point
# ---------------------------------------------------------------------------


def get_nova_chatbot_images(files_found, report_folder, seeker, wrap_text):
    for file_found in files_found:
        file_found = str(file_found)
        if not file_found.endswith("chat-ai.db"):
            continue

        try:
            db = sqlite3.connect(file_found)
            cursor = db.cursor()
            cursor.execute(QUERY)
            rows_raw = cursor.fetchall()
            db.close()
        except Exception as e:
            scripts.ilapfuncs.logfunc(
                f"[nova_chatbot_images] Error reading {file_found}: {e}"
            )
            continue

        if not rows_raw:
            scripts.ilapfuncs.logfunc(
                f"[nova_chbot_images] No image records found in {file_found}."
            )
            continue

        images_dir = os.path.join(report_folder, "nova_images")
        os.makedirs(images_dir, exist_ok=True)

        headers = [
            "Image ID",
            "Msg. ID",
            "Conv. ID",
            "Conv. UUID",
            "Conv. Title",
            "AI Model",
            "Conv. Deleted",
            "Msg. Role",
            "Msg. Text",
            "Msg. Timestamp (UTC)",
            "Image Preview & Metadata",
            "Prompt",
            "State",
            "Pipeline",
            "Style ID",
            "MIME Type",
            "Firebase Path",
        ]

        html_rows = []
        tsv_rows = []

        for row in rows_raw:
            (
                img_id,
                msg_id,
                img_url,
                prompt,
                state,
                mime_type,
                style_id,
                source,
                source_url,
                pipeline,
                conv_id,
                msg_type,
                msg_text,
                msg_created_at,
                conv_uuid,
                conv_title,
                chat_bot_model,
                soft_deleted,
            ) = row

            filename = _resolve_image_file(img_url, seeker, images_dir)

            common = (
                img_id,
                msg_id,
                conv_id,
                conv_uuid or "",
                conv_title or "",
                _resolve_model(chat_bot_model),
                _format_soft_deleted(soft_deleted),
                _format_role(msg_type),
                msg_text or "",
                _convert_ms_timestamp(msg_created_at),
            )

            img_cell = _build_image_cell(
                img_url,
                prompt,
                state,
                mime_type,
                pipeline,
                style_id,
                msg_type,
                filename,
            )

            # For TSV we also include the origin (derived from msg_type)
            origin_label = (
                "User-submitted"
                if msg_type == 0
                else "AI-generated"
                if msg_type == 1
                else "Unknown"
            )

            html_rows.append(
                common
                + (
                    img_cell,
                    prompt or "",
                    _resolve_state(state),
                    pipeline or "",
                    style_id if style_id is not None else "",
                    mime_type or "",
                    img_url or "",
                )
            )

            tsv_rows.append(
                common
                + (
                    "",  # no HTML in TSV
                    prompt or "",
                    _resolve_state(state),
                    pipeline or "",
                    style_id if style_id is not None else "",
                    mime_type or "",
                    img_url or "",
                )
            )

        report_name = "Nova AI Chatbot - HistoryDetailImage"
        report = ArtifactHtmlReport(report_name)
        report.start_artifact_report(report_folder, report_name)
        report.add_script()
        report.write_artifact_data_table(
            headers, html_rows, file_found, html_escape=False
        )
        report.end_artifact_report()

        scripts.ilapfuncs.tsv(report_folder, headers, tsv_rows, report_name, file_found)
        scripts.ilapfuncs.timeline(report_folder, report_name, tsv_rows, headers)
